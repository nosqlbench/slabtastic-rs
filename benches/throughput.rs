/// Spawn `n_threads` threads, each opening its own `SlabReader` on the
/// same file and performing `reads_per_thread` random `get()` calls.
///
/// Each thread uses a different seed (`thread_index * 6271`) so that
/// threads hit different pages and exercise OS-level read contention
/// rather than all hitting the same cached page.
///
/// Note: `SlabReader` is not `Send`, so each thread must open its own
/// reader. The per-thread open cost is included in the measurement.
fn concurrent_random_reads(path: &Path, n_threads: usize, reads_per_thread: u64, record_count: u64) {
    let path = Arc::new(path.to_path_buf());
    let handles: Vec<_> = (0..n_threads)
        .map(|t| {
            let path = Arc::clone(&path);
            thread::spawn(move || {
                let mut reader = SlabReader::open(&*path).unwrap();
                let seed = (t as u64).wrapping_mul(6271);
                for i in 0..reads_per_thread {
                    let ordinal = ((i.wrapping_mul(7919).wrapping_add(seed)) % record_count) as i64;
                    let _ = reader.get(ordinal).unwrap();
                }
            })
        })
        .collect();
    for h in handles {
        h.join().unwrap();
    }
}

/// Measure concurrent random-access read scaling (2/4/8 threads).
///
/// All threads share the same pre-built 10 M-record file, each with its
/// own `SlabReader` and file descriptor. Throughput is reported as the
/// aggregate number of `get()` calls across all threads per second. Ideal
/// scaling would show near-linear throughput increase, while lower values
/// indicate I/O or page-cache contention.
///
/// Per-thread `SlabReader::open` cost is included since each thread needs
/// its own file descriptor; this is the realistic concurrent access pattern.
fn bench_concurrent_random_read(c: &mut Criterion) {
    let mut group = c.benchmark_group("concurrent_random_read");
    group.sample_size(10);

    let record_count = 10_000_000u64;
    let (_tmp, path) = prepare_file(record_count);
    let reads_per_thread = 10_000u64;

    for n_threads in [2, 4, 8] {
        let total_reads = reads_per_thread * n_threads as u64;
        group.throughput(Throughput::Elements(total_reads));
        group.bench_with_input(
            BenchmarkId::new("threads", n_threads),
            &n_threads,
            |b, &n_threads| {
                b.iter(|| {
                    concurrent_random_reads(&path, n_threads, reads_per_thread, record_count);
                });
            },
        );
    }
    group.finish();
}

/// Spawn `n_threads` threads, each opening its own `SlabReader` and
/// iterating every record via `iter()`.
///
/// Per-thread open cost is included since `SlabReader` is not `Send`.
fn concurrent_sequential_reads(path: &Path, n_threads: usize) {
    let path = Arc::new(path.to_path_buf());
    let handles: Vec<_> = (0..n_threads)
        .map(|_| {
            let path = Arc::clone(&path);
            thread::spawn(move || {
                let mut reader = SlabReader::open(&*path).unwrap();
                let records = reader.iter().unwrap();
                assert!(!records.is_empty());
            })
        })
        .collect();
    for h in handles {
        h.join().unwrap();
    }
}

/// Measure concurrent sequential read scaling (2/4/8 threads).
///
/// All threads share the same pre-built 1 M-record file and each
/// performs a full `iter()`. Throughput is reported as the aggregate
/// record count across all threads per second. This stresses sequential
/// I/O bandwidth when multiple readers compete for the same file.
///
/// Per-thread `SlabReader::open` cost is included since each thread needs
/// its own file descriptor.
fn bench_concurrent_sequential_read(c: &mut Criterion) {
    let mut group = c.benchmark_group("concurrent_sequential_read");
    group.sample_size(10);

    let record_count = 1_000_000u64;
    let (_tmp, path) = prepare_file(record_count);

    for n_threads in [2, 4, 8] {
        let total = record_count * n_threads as u64;
        group.throughput(Throughput::Elements(total));
        group.bench_with_input(
            BenchmarkId::new("threads", n_threads),
            &n_threads,
            |b, &n_threads| {
                b.iter(|| {
                    concurrent_sequential_reads(&path, n_threads);
                });
            },
        );
    }
    group.finish();
}

/// Spawn `n_threads` threads, each writing its own independent slab file
/// with `records_per_thread` records, then closing it.
fn concurrent_writes(n_threads: usize, records_per_thread: u64) {
    let handles: Vec<_> = (0..n_threads)
        .map(|t| {
            thread::spawn(move || {
                let tmp = NamedTempFile::new().unwrap();
                let path = tmp.path().to_path_buf();
                let config = WriterConfig::default();
                let mut writer = SlabWriter::new(&path, config).unwrap();
                let base = t as u64 * records_per_thread;
                for i in 0..records_per_thread {
                    writer.add_record(&make_record(base + i)).unwrap();
                }
                writer.finish().unwrap();
            })
        })
        .collect();
    for h in handles {
        h.join().unwrap();
    }
}

/// Measure concurrent independent-file write scaling (2/4/8 threads).
///
/// Each thread creates and writes its own temp file with 1 M records.
/// Throughput is reported as the aggregate record count across all
/// threads per second. Since each thread writes to a different file,
/// this measures how well the filesystem handles parallel write streams
/// without file-level lock contention.
fn bench_concurrent_write(c: &mut Criterion) {
    let mut group = c.benchmark_group("concurrent_write");
    group.sample_size(10);

    let records_per_thread = 1_000_000u64;

    for n_threads in [2, 4, 8] {
        let total = records_per_thread * n_threads as u64;
        group.throughput(Throughput::Elements(total));
        group.bench_with_input(
            BenchmarkId::new("threads", n_threads),
            &n_threads,
            |b, &n_threads| {
                b.iter(|| {
                    concurrent_writes(n_threads, records_per_thread);
                });
            },
        );
    }
    group.finish();
}

