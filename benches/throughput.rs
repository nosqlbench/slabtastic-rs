/// Measure batched-iteration throughput via `SlabReader::batch_iter`.
///
/// The file is pre-built with `count` records. Each iteration opens a
/// fresh reader, converts it to a `SlabBatchIter` with the given
/// `batch_size`, and drains all batches. Throughput is records per second.
fn bench_batch_iter(c: &mut Criterion) {
    let mut group = c.benchmark_group("batch_iter");
    group.sample_size(10);

    let count = 1_000_000u64;
    let (_tmp, path) = prepare_file(count);

    for batch_size in [64, 1024, 16384] {
        group.throughput(Throughput::Elements(count));
        group.bench_with_input(
            BenchmarkId::new("batch_size", batch_size),
            &batch_size,
            |b, &batch_size| {
                b.iter(|| {
                    let reader = SlabReader::open(&path).unwrap();
                    let mut iter = reader.batch_iter(batch_size);
                    let mut total = 0u64;
                    loop {
                        let batch = iter.next_batch().unwrap();
                        if batch.is_empty() {
                            break;
                        }
                        total += batch.len() as u64;
                    }
                    assert_eq!(total, count);
                });
            },
        );
    }
    group.finish();
}

/// Measure synchronous sink-read throughput via `read_all_to_sink`.
///
/// Reads all records from a pre-built file into a `Vec<u8>` sink.
/// Throughput is records per second.
fn bench_read_all_to_sink(c: &mut Criterion) {
    let mut group = c.benchmark_group("read_all_to_sink");
    group.sample_size(10);

    for count in [1_000_000u64, 10_000_000] {
        let (_tmp, path) = prepare_file(count);

        group.throughput(Throughput::Elements(count));
        group.bench_with_input(
            BenchmarkId::new("records", count),
            &count,
            |b, &_count| {
                b.iter(|| {
                    let mut reader = SlabReader::open(&path).unwrap();
                    let mut sink = Vec::new();
                    let n = reader.read_all_to_sink(&mut sink).unwrap();
                    assert!(n > 0);
                });
            },
        );
    }
    group.finish();
}

/// Measure async sink-read throughput via `read_to_sink_async`.
///
/// Spawns a background thread to read all records from a pre-built file
/// into a `Vec<u8>` sink, then waits for completion. This measures the
/// full callback-mode lifecycle: thread spawn, file open on the
/// background thread, sequential iteration with progress updates, and
/// join.
fn bench_read_to_sink_async(c: &mut Criterion) {
    let mut group = c.benchmark_group("read_to_sink_async");
    group.sample_size(10);

    for count in [1_000_000u64, 10_000_000] {
        let (_tmp, path) = prepare_file(count);

        group.throughput(Throughput::Elements(count));
        group.bench_with_input(
            BenchmarkId::new("records", count),
            &count,
            |b, &_count| {
                b.iter(|| {
                    let sink: Vec<u8> = Vec::new();
                    let task = SlabReader::read_to_sink_async(
                        path.clone(),
                        sink,
                        |_| {},
                    );
                    let n = task.wait().unwrap();
                    assert!(n > 0);
                });
            },
        );
    }
    group.finish();
}

/// Measure async iterator-write throughput via `write_from_iter_async`.
///
/// Spawns a background thread that consumes an iterator of `count`
/// records and writes them to a fresh slab file. Measures the full
/// callback-mode write lifecycle including thread spawn, file creation,
/// record iteration with progress updates, finish, and join.
fn bench_write_from_iter_async(c: &mut Criterion) {
    let mut group = c.benchmark_group("write_from_iter_async");
    group.sample_size(10);

    for count in [1_000_000u64, 10_000_000] {
        group.throughput(Throughput::Elements(count));
        group.bench_with_input(
            BenchmarkId::new("records", count),
            &count,
            |b, &count| {
                b.iter(|| {
                    let tmp = NamedTempFile::new().unwrap();
                    let path = tmp.path().to_path_buf();
                    let iter = (0..count).map(make_record);
                    let task = SlabWriter::write_from_iter_async(
                        path,
                        WriterConfig::default(),
                        iter,
                        |_| {},
                    );
                    let n = task.wait().unwrap();
                    assert_eq!(n, count);
                });
            },
        );
    }
    group.finish();
}

/// Measure bulk-write throughput via `add_records`.
///
/// Pre-generates all records, then writes them in a single
/// `add_records` call. Contrasts with the single-record `bench_write`
/// to show the overhead (or lack thereof) of the bulk API.
fn bench_bulk_write(c: &mut Criterion) {
    let mut group = c.benchmark_group("bulk_write");
    group.sample_size(10);

    for count in [1_000_000u64] {
        let records: Vec<Vec<u8>> = (0..count).map(make_record).collect();
        let refs: Vec<&[u8]> = records.iter().map(|r| r.as_slice()).collect();

        group.throughput(Throughput::Elements(count));
        group.bench_with_input(
            BenchmarkId::new("records", count),
            &count,
            |b, &_count| {
                b.iter(|| {
                    let tmp = NamedTempFile::new().unwrap();
                    let path = tmp.path().to_path_buf();
                    let mut writer = SlabWriter::new(&path, WriterConfig::default()).unwrap();
                    writer.add_records(&refs).unwrap();
                    writer.finish().unwrap();
                });
            },
        );
    }
    group.finish();
}

