//! Throughput benchmarks for slabtastic write and read paths.
//!
//! ## Benchmark groups
//!
//! **Single-threaded:**
//! - `write` — end-to-end write throughput (file create → add_record × N → finish)
//! - `sequential_read` — full-file iteration via `SlabReader::iter`
//! - `random_read` — 10 000 point lookups via `SlabReader::get` with
//!   pseudo-random ordinals spread across the file
//!
//! **Concurrent (multi-threaded):**
//! - `concurrent_random_read` — N threads (2/4/8) each open an independent
//!   `SlabReader` on the same 10 M-record file and perform 10 000 random
//!   `get()` calls with different access patterns. Measures OS page-cache
//!   and file-I/O scaling under read contention.
//! - `concurrent_sequential_read` — N threads (2/4/8) each open their own
//!   `SlabReader` on the same 1 M-record file and call `iter()` to read
//!   every record. Measures sequential bandwidth scaling.
//! - `concurrent_write` — N threads (2/4/8) each write an independent
//!   1 M-record slab file in parallel. Measures aggregate write throughput
//!   when multiple pipelines run simultaneously.
//!
//! Single-threaded read benchmarks use `iter_batched` to open the file in
//! the setup phase (outside the timed region) so that only the actual
//! read operations are measured. Concurrent benchmarks necessarily include
//! per-thread open cost since `SlabReader` is not `Send`.
//!
//! All benchmarks use deterministic 5–25 byte records generated by
//! [`make_record`]. For 100 M+ scale tests see `tests/large_scale.rs`.

use std::path::{Path, PathBuf};
use std::sync::Arc;
use std::thread;

use criterion::{BatchSize, BenchmarkId, Criterion, Throughput, criterion_group, criterion_main};
use slabtastic::{SlabReader, SlabWriter, WriterConfig};
use tempfile::NamedTempFile;

/// Generate a deterministic record of 5–25 bytes based on ordinal.
///
/// The length cycles through 5..=25 (mod 21) so that page packing is
/// exercised with non-uniform record sizes, matching realistic short-text
/// workloads.
fn make_record(ordinal: u64) -> Vec<u8> {
    let len = 5 + (ordinal % 21) as usize;
    let base = format!("{ordinal:0>width$}", width = len);
    base.into_bytes()[..len].to_vec()
}

/// Write a test file with `count` records using default config and return
/// the `NamedTempFile` handle (must be kept alive to prevent deletion)
/// together with the file path.
fn prepare_file(count: u64) -> (NamedTempFile, PathBuf) {
    let tmp = NamedTempFile::new().unwrap();
    let path = tmp.path().to_path_buf();
    let config = WriterConfig::default();
    let mut writer = SlabWriter::new(&path, config).unwrap();
    for i in 0..count {
        writer.add_record(&make_record(i)).unwrap();
    }
    writer.finish().unwrap();
    (tmp, path)
}

// ---------------------------------------------------------------------------
// Single-threaded benchmarks
// ---------------------------------------------------------------------------

/// Measure single-threaded write throughput.
///
/// Each iteration creates a fresh temp file, writes `count` records with
/// default page config (64 KiB preferred), then calls `finish()`. The
/// throughput metric is records per second. File creation is included in
/// the measurement since it is part of the write lifecycle.
fn bench_write(c: &mut Criterion) {
    let mut group = c.benchmark_group("write");
    group.sample_size(10);

    for count in [1_000_000u64, 10_000_000] {
        group.throughput(Throughput::Elements(count));
        group.bench_with_input(
            BenchmarkId::new("records", count),
            &count,
            |b, &count| {
                b.iter(|| {
                    let tmp = NamedTempFile::new().unwrap();
                    let path = tmp.path().to_path_buf();
                    let config = WriterConfig::default();
                    let mut writer = SlabWriter::new(&path, config).unwrap();
                    for i in 0..count {
                        writer.add_record(&make_record(i)).unwrap();
                    }
                    writer.finish().unwrap();
                });
            },
        );
    }
    group.finish();
}

/// Measure single-threaded sequential read throughput via `iter()`.
///
/// The file is opened in the setup phase (outside the timed region) so
/// that only the page-by-page deserialization and record extraction are
/// measured. Uses `iter_batched` with `LargeInput` batch size since each
/// setup allocates a `SlabReader` with its file descriptor and in-memory
/// pages-page index.
fn bench_sequential_read(c: &mut Criterion) {
    let mut group = c.benchmark_group("sequential_read");
    group.sample_size(10);

    for count in [1_000_000u64, 10_000_000] {
        let (_tmp, path) = prepare_file(count);

        group.throughput(Throughput::Elements(count));
        group.bench_with_input(
            BenchmarkId::new("iter", count),
            &count,
            |b, &_count| {
                b.iter_batched(
                    || SlabReader::open(&path).unwrap(),
                    |mut reader| {
                        let records = reader.iter().unwrap();
                        assert!(!records.is_empty());
                    },
                    BatchSize::LargeInput,
                );
            },
        );
    }
    group.finish();
}

/// Measure single-threaded random-access read throughput via `get()`.
///
/// 10 000 pseudo-random ordinals (deterministic, using prime-modular
/// scatter) are looked up against a pre-built file. The file is opened
/// in the setup phase so that only the binary-search index lookup plus
/// single-page deserialization cost is measured per `get()` call.
fn bench_random_read(c: &mut Criterion) {
    let mut group = c.benchmark_group("random_read");
    group.sample_size(10);

    for count in [1_000_000u64, 10_000_000] {
        let (_tmp, path) = prepare_file(count);

        let sample_count = 10_000u64;
        let ordinals: Vec<i64> = (0..sample_count)
            .map(|i| ((i * 7919) % count) as i64)
            .collect();

        group.throughput(Throughput::Elements(sample_count));
        group.bench_with_input(
            BenchmarkId::new("get_10k", count),
            &ordinals,
            |b, ordinals| {
                b.iter_batched(
                    || SlabReader::open(&path).unwrap(),
                    |mut reader| {
                        for &ord in ordinals {
                            let _ = reader.get(ord).unwrap();
                        }
                    },
                    BatchSize::LargeInput,
                );
            },
        );
    }
    group.finish();
}

